<p>What exactly does Howard Phillips Lovecraft have to do with philosophy? Lovecraft through
his writing was able to show man just how feeble we truely are. This school of thought has been labeled
as Cosmicism. On a grand scale humans and the earth itself are of little importance to the cosmos;
 It is hard to argue with such an idea. <q cite="https://en.wikipedia.org/wiki/Cosmicism">
  <small>The human race will disappear. Other races will appear and disappear in turn.
    The sky will become icy and void, pierced by the feeble light
  of half-dead stars. Which will also disappear. Everything will disappear. And what human
  beings do is just as free of sense as the free motion of elementary particles. Good, evil, morality,
  feelings? Pure &#39;Victorian fictions&#39;. Only egotism exists. - HP Lovecraft</small></q>
Sometimes we forget our place. Often it is hard to think that a quasar can come right this very instant
and destory our entire planet. The thing that tells us that we are important is our ego. Our
<a href="https://www.amazon.com/Ego-Enemy-Ryan-Holiday/dp/1591847818">ego is the enemy.</a>
Ego death is often refered to as the <q>great death</q> in comparison to a physical death often described
as <q>small death</q>. It is important to understand this basic fundamental thought as it relates to
stoicism.</p>


